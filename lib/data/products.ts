// Helper utility to sanitize any product item to guarantee compatibility with layout views
export const mapProduct = (p: any): Product => {
  if (!p) return {} as Product;
  return {
    id: p.id || p.productId || '',
    productId: p.productId || p.id || '',
    name: p.name || p.title || '',
    title: p.title || p.name || '',
    slug: p.slug || '',
    category: p.category || '',
    images: p.images || (p.image ? [p.image] : []),
    color: p.color || (p.colors && p.colors[0]) || '',
    colors: p.colors || (p.color ? [p.color] : []),
    mrpPrice: p.mrpPrice || p.price || 0,
    price: p.price || p.mrpPrice || 0,
    sellingPrice: p.sellingPrice || p.discountedPrice || p.price || 0,
    discountedPrice: p.discountedPrice || p.sellingPrice || p.price || 0,
    discountPercent: p.discountPercent || p.discount || 0,
    label: p.label || (p.isNew ? 'NEW' : p.bestSeller ? 'BEST SELLER' : ''),
    description: p.description || '',
    sizes: p.sizes
      ? Array.isArray(p.sizes) && typeof p.sizes[0] === 'object'
        ? p.sizes
        : p.sizes.map((s: any) => ({ size: typeof s === 'string' ? s : s.size || '', stock: typeof s === 'string' ? 10 : s.stock || 0 }))
      : [],
    brand: p.brand || 'GR Styles',
    rating: p.rating || 4.5,
    reviews: p.reviews || 0,
    isNew: p.isNew || p.label === 'NEW',
    bestSeller: p.bestSeller || p.label === 'BEST SELLER',
  };
};

// Array of all products mapped correctly
export const products: Product[] = Object.values(productDatabase).map(mapProduct);

// Get product by slug
export const getProductData = (slug: string): Product | null => {
  const prod = productDatabase[slug];
  return prod ? mapProduct(prod) : null;
};

// Get related products
export const getRelatedProducts = (currentSlug: string): Product[] => {
  return Object.values(productDatabase)
    .filter((p: Product) => p.slug !== currentSlug)
    .slice(0, 4)
    .map(mapProduct);
};
